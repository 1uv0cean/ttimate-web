/**
 * 띠 조합에 따른 상세 궁합 정보를 가져옵니다
 * @param animal1 첫 번째 띠
 * @param animal2 두 번째 띠
 * @returns 상세 궁합 정보 또는 null
 */
export function getDetailedCompatibility(animal1: ZodiacAnimal, animal2: ZodiacAnimal): DetailedCompatibility | null {
  const pair1 = `${animal1}띠-${animal2}띠`;
  const pair2 = `${animal2}띠-${animal1}띠`;
  
  return DETAILED_COMPATIBILITY_DATA.find(data => data.pair === pair1 || data.pair === pair2) || null;
}

/**
 * 모든 띠별 궁합 조합 키를 생성합니다
 * @returns 모든 조합의 키 배열
 */
export function getAllCompatibilityPairs(): string[] {
  return DETAILED_COMPATIBILITY_DATA.map(data => data.pair);
}

/**
 * 특정 띠의 모든 궁합 정보를 가져옵니다
 * @param animal 띠
 * @returns 해당 띠의 모든 궁합 정보
 */
export function getCompatibilityForAnimal(animal: ZodiacAnimal): DetailedCompatibility[] {
  return DETAILED_COMPATIBILITY_DATA.filter(data => 
    data.pair.startsWith(`${animal}띠-`) || data.pair.endsWith(`-${animal}띠`)
  );
}